// 작품 검색 및 조회 유틸리티 함수들
const getWorkByCategory = (category) => {
  return worksData[category] || [];
};

const getWorkById = (category, id) => {
  const works = worksData[category] || [];
  return works.find((work) => work.id === parseInt(id));
};

const getAllWorks = () => {
  return Object.values(worksData).flat();
};

const getArtistWorks = (artistName) => {
  const allWorks = getAllWorks();
  return allWorks.filter((work) => work.artist === artistName);
};

const getCategoryName = (category) => {
  const categoryNames = {
    clo: '클로',
    traditional: '전통복식',
    works: '작품',
    'media-production': '미디어제작',
    'fashion-marketing': '패션마케팅',
  };
  return categoryNames[category] || category;
};

